var myFruitArray = ["bananas", "apple", "orange"]

// print all the elements in the array, but not one by one
for fruit in myFruitArray {
    print(fruit)
}

var myNumbers = [10, 20, 30, 40, 50, 60]

for number in myNumbers {
    print(number / 5) // apply operation to each number in the array
}


// loop through number sequence
print("\n")
for i in 1...5 {
    print(i)
}
